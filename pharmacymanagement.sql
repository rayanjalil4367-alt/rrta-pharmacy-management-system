CREATE DATABASE IF NOT EXISTS PharmacyManagementSystem;
USE PharmacyManagementSystem;─────────────────────────────────────────────

CREATE TABLE Customer (
    customer_id INT PRIMARY KEY,
    C_Name      VARCHAR(100) NOT NULL,
    Phone_no    VARCHAR(20)  NOT NULL,
    Address     VARCHAR(200) NOT NULL
);

CREATE TABLE Pharmacist (
    Pharmacist_id INT          PRIMARY KEY,
    P_Name        VARCHAR(100) NOT NULL,
    Phone_no      VARCHAR(20)  NOT NULL,
    Email         VARCHAR(100) NOT NULL UNIQUE,
    Salary        DECIMAL(10,2) NOT NULL CHECK (Salary > 0)
);

CREATE TABLE Category (
    Category_id   INT          PRIMARY KEY,
    Category_name VARCHAR(100) NOT NULL UNIQUE,
    Description   VARCHAR(200) NOT NULL
);

CREATE TABLE Medicine (
    Medicine_id  INT          PRIMARY KEY,
    Name         VARCHAR(100) NOT NULL,
    Batch_no     VARCHAR(50)  NOT NULL UNIQUE,
    Expiry_date  DATE         NOT NULL,
    Price        DECIMAL(10,2) NOT NULL CHECK (Price > 0),
    Category_id  INT          NOT NULL,
    FOREIGN KEY (Category_id) REFERENCES Category(Category_id) ON DELETE CASCADE
);

CREATE TABLE Supplier (
    Supplier_id INT          PRIMARY KEY,
    S_Name      VARCHAR(100) NOT NULL,
    Phone_no    VARCHAR(20)  NOT NULL,
    Email       VARCHAR(100) NOT NULL UNIQUE,
    Address     VARCHAR(200) NOT NULL
);

CREATE TABLE Inventory (
    Inventory_id    INT  PRIMARY KEY,
    Medicine_id     INT  NOT NULL,
    Supplier_id     INT  NOT NULL,
    Stock_quantity  INT  NOT NULL CHECK (Stock_quantity >= 0),
    Reorder_level   INT  NOT NULL CHECK (Reorder_level >= 0),
    Last_restocked  DATE NOT NULL,
    FOREIGN KEY (Medicine_id) REFERENCES Medicine(Medicine_id) ON DELETE CASCADE,
    FOREIGN KEY (Supplier_id) REFERENCES Supplier(Supplier_id) ON DELETE CASCADE
);

CREATE TABLE Product_Sales (
    P_Sales_id    INT           PRIMARY KEY,
    customer_id   INT           NOT NULL,
    Pharmacist_id INT           NOT NULL,
    Date          DATE          NOT NULL,
    Amount        DECIMAL(10,2) NOT NULL CHECK (Amount > 0),
    FOREIGN KEY (customer_id)   REFERENCES Customer(customer_id)   ON DELETE CASCADE,
    FOREIGN KEY (Pharmacist_id) REFERENCES Pharmacist(Pharmacist_id) ON DELETE CASCADE
);

CREATE TABLE Sale_Items (
    item_id     INT PRIMARY KEY AUTO_INCREMENT,
    P_Sales_id  INT NOT NULL,
    Medicine_id INT NOT NULL,
    Quantity    INT NOT NULL CHECK (Quantity > 0),
    FOREIGN KEY (P_Sales_id)  REFERENCES Product_Sales(P_Sales_id) ON DELETE CASCADE,
    FOREIGN KEY (Medicine_id) REFERENCES Medicine(Medicine_id)      ON DELETE CASCADE
);

CREATE TABLE Payment (
    p_id           INT           PRIMARY KEY,
    P_Sales_id     INT           NOT NULL,
    Date           DATE          NOT NULL,
    Payment_method VARCHAR(50)   NOT NULL,
    amount_paid    DECIMAL(10,2) NOT NULL CHECK (amount_paid > 0),
    FOREIGN KEY (P_Sales_id) REFERENCES Product_Sales(P_Sales_id) ON DELETE CASCADE
);

